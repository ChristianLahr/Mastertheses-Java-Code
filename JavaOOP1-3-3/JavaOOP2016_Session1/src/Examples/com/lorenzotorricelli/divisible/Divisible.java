package com.lorenzotorricelli.divisible;

public class Divisible {

	public static void main(String[] args) { //A classic if-else loov
		int dividend=10;
		int divisor=2;
		if(dividend%divisor==0){
			System.out.println(dividend + "is divisible by " + divisor); 
		}else{
			System.out.println(dividend + "is divisible by " + divisor); 

		}

	}
	
}
